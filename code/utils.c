#include "../header/pipex.h"

int	flags(char *str)
{
	if (!str)
		return (0);
	if (str[0] == '>')
	{
		if (str[1] == '>')
			return (001 | 0100);
		return (001 | 0100 | 01000);
	}
	return (0);
}